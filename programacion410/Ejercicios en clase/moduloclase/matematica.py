"""modulo de operaciones matematicas basicas
suma, resta, multipicacion y division"""

def suma(a,b): #retorna la suma de dos numeros
    return a+b

def resta(a,b): #retorna la resta de dos numeros
    return a-b

def multiplicacion(a,b): #retorna la multiplicacion de dos numeros
    return a*b

def division(a,b): #retorna la division de dos numeros
    return a/b
